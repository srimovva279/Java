package org.ProgrammingClass;

public class EvenNUM {
	
		private static void evenNum() {
				
		for (int i = 0; i <=10; i++) {
			
			if (i%2==0)
			{
				System.out.println("even number"+i);
			}
			else
			{
				System.out.println("odd number"+i);
			}
		}
		
		}
			public static void main(String[] args) {
				
				evenNum();
			}
		
	}


